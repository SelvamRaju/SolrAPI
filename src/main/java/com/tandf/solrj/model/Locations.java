package com.tandf.solrj.model;

public class Locations {

	private String statusCode;

    private String discountGroup;

    private String status;

    private String distributionCenter;

    private String distributionCenterCode;

    private String discountGroupCode;

	public String getStatusCode() {
		return statusCode;
	}

	public void setStatusCode(String statusCode) {
		this.statusCode = statusCode;
	}

	public String getDiscountGroup() {
		return discountGroup;
	}

	public void setDiscountGroup(String discountGroup) {
		this.discountGroup = discountGroup;
	}


	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getDistributionCenter() {
		return distributionCenter;
	}

	public void setDistributionCenter(String distributionCenter) {
		this.distributionCenter = distributionCenter;
	}

	public String getDistributionCenterCode() {
		return distributionCenterCode;
	}

	public void setDistributionCenterCode(String distributionCenterCode) {
		this.distributionCenterCode = distributionCenterCode;
	}

	public String getDiscountGroupCode() {
		return discountGroupCode;
	}

	public void setDiscountGroupCode(String discountGroupCode) {
		this.discountGroupCode = discountGroupCode;
	}
    
    
}
